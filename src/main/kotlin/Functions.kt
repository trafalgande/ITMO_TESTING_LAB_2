abstract class Functions {
    abstract fun sin(x: Double, eps: Double = 0.000001): Double
    abstract fun cos(x: Double, eps: Double = 0.000001): Double
    abstract fun sec(x: Double, eps: Double = 0.000001): Double
    abstract fun csc(x: Double, eps: Double = 0.000001): Double
    abstract fun log(x: Double, base: Double, eps: Double = 0.000001): Double
    abstract fun log_2(x: Double, eps: Double = 0.000001): Double
    abstract fun log_3(x: Double, eps: Double = 0.000001): Double
    abstract fun log_5(x: Double, eps: Double = 0.000001): Double
    abstract fun log_10(x: Double, eps: Double = 0.000001): Double
    abstract fun ln(x: Double, eps: Double = 0.000001): Double
}